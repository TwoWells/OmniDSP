  // --- Factory Function ---
  /**
   * @brief Factory function to create an OmniDSP instance with a specific
   * backend.
   * @param backend The desired backend implementation (OmniDSP::Backend enum).
   * @return An OmniExpected containing the OmniDSP instance on success, or a
   * Status code on failure.
   */
  // Definition matches declaration: static OmniExpected<OmniDSP> create(Backend
  // backend); Default argument is only in the declaration.
  [[nodiscard]] /* static */ OmniExpected<OmniDSP> OmniDSP::create(
      BackendType backend)
  {
    // Pointer to the abstract backend implementation (using type from
    // interface/backend.hpp)
    std::unique_ptr<Abstract::Backend> pimpl = nullptr;

    try {
      // Select the backend factory based on the requested type and availability
      switch (backend) {  // Switching on the 'Backend' enum parameter
        case BackendType::Accelerate:  // Use 'Backend' enum value
          // Use if constexpr to check if the backend is enabled at compile time
          if constexpr (OMNIDSP_BACKEND_ACCELERATE_ENABLED) {
            std::cout << "Creating Accelerate backend..." << std::endl;
            // Call factory function from abstract namespace
            pimpl = Abstract::create_accelerate_backend();
            if (!pimpl) {  // Check if factory succeeded at runtime
              std::cerr << "Error: Accelerate backend factory failed."
                        << std::endl;
              return std::unexpected(Status::BackendError);
            }
          }
          else {
            std::cerr
                << "Error: Accelerate backend requested but not compiled in."
                << std::endl;
            return std::unexpected(Status::UnsupportedFeature);
          }
          break;  // End Accelerate case

        case BackendType::OneMKL:  // Use 'Backend' enum value
          if constexpr (OMNIDSP_BACKEND_ONEMKL_ENABLED) {
            std::cout << "Creating OneMKL backend..." << std::endl;
            // Call factory function from abstract namespace
            pimpl = Abstract::create_onemkl_backend();
            if (!pimpl) {
              std::cerr << "Error: OneMKL backend factory failed." << std::endl;
              return std::unexpected(Status::BackendError);
            }
          }
          else {
            std::cerr << "Error: oneMKL backend requested but not compiled in."
                      << std::endl;
            return std::unexpected(Status::UnsupportedFeature);
          }
          break;  // End OneMKL case

        case BackendType::IntelIPP:  // Use 'Backend' enum value
          if constexpr (OMNIDSP_BACKEND_INTELIPP_ENABLED) {
            std::cout << "Creating IntelIPP backend..." << std::endl;
            // Call factory function from abstract namespace
            pimpl = Abstract::create_intelipp_backend();
            if (!pimpl) {
              std::cerr << "Error: IntelIPP backend factory failed."
                        << std::endl;
              return std::unexpected(Status::BackendError);
            }
          }
          else {
            std::cerr
                << "Error: IntelIPP backend requested but not compiled in."
                << std::endl;
            return std::unexpected(Status::UnsupportedFeature);
          }
          break;  // End IntelIPP case

        case BackendType::Default:  // Use 'Backend' enum value
        default:                    // Fallback to Default
          // Default backend should ideally always be enabled
          if constexpr (OMNIDSP_BACKEND_DEFAULT_ENABLED) {
            std::cout << "Creating Default backend..." << std::endl;
            // Call factory function from abstract namespace
            pimpl = Abstract::create_default_backend();
            if (!pimpl) {
              std::cerr << "Error: Default backend factory failed."
                        << std::endl;
              return std::unexpected(Status::BackendError);
            }
          }
          else {
            // This indicates a build system configuration error
            std::cerr << "Error: Default backend requested but not compiled in "
                         "(OMNIDSP_BACKEND_DEFAULT_ENABLED is false)."
                      << std::endl;
            return std::unexpected(Status::BackendError);
          }
          break;  // End Default case
      }  // End switch

      // If we fall through the switch without returning an error, pimpl should
      // be valid.
      if (!pimpl) {
        // This case should ideally not be reached if all factories are checked
        std::cerr << "Internal Error: Backend selection failed unexpectedly."
                  << std::endl;
        return std::unexpected(Status::Failure);
      }

      std::cout
          << "Backend created successfully. Constructing OmniDSP instance."
          << std::endl;

      // Use private constructor (takes unique_ptr<abstract::AbstractBackend>)
      OmniDSP dsp_instance(std::move(pimpl));

      // Return the successfully created instance wrapped in OmniExpected
      return dsp_instance;
    }
    // Catch specific exceptions first if they map to specific Status codes
    catch (const std::bad_alloc &e) {
      std::cerr << "Error: Memory allocation failed during backend creation: "
                << e.what() << std::endl;
      return std::unexpected(Status::AllocationError);
    }
    // Catch any other std::exception (including derived classes like
    // std::runtime_error and potentially OmniDSPException if it inherits from
    // it)
    catch (const std::exception &e) {
      // Log the actual error message from the exception
      std::cerr << "Error: Exception during backend initialization: "
                << e.what() << std::endl;
      // Return a general backend error status
      return std::unexpected(Status::BackendError);
    }
    // Catch any other unknown exceptions (non-std::exception based)
    catch (...) {
      std::cerr << "Error: Unknown exception during backend creation."
                << std::endl;
      return std::unexpected(
          Status::Failure);  // Generic failure for unknown errors
    }
  }

