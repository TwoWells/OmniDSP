  /**
   * @brief Gets the type of the backend currently used by this OmniDSP
   * instance.
   * @return The Backend enum value.
   * @throws std::runtime_error if the instance is in an invalid (e.g.,
   * moved-from) state.
   */
  // Definition matches declaration: Backend get_backend() const;
  BackendType OmniDSP::get_backend() const
  {
    if (!pimpl_) {
      // Throw here as using a moved-from object is a programming error
      throw std::runtime_error(
          "Invalid OmniDSP instance in get_backend (possibly moved-from).");
    }
    // Call the get_backend() method from the AbstractBackend interface.
    return pimpl_->get_backend();
  }

